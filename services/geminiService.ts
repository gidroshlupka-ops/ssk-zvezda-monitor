import { GoogleGenAI } from "@google/genai";

const getClient = () => {
  const apiKey = process.env.API_KEY;
  if (!apiKey) {
    console.error("API_KEY is missing via process.env.API_KEY");
    return null;
  }
  return new GoogleGenAI({ apiKey });
};

export const analyzeProductionData = async (dataJSON: string): Promise<string> => {
  const ai = getClient();
  if (!ai) return "Ошибка: API ключ не найден. Пожалуйста, настройте окружение.";

  const prompt = `
    Вы - эксперт по анализу производственных процессов и финансовому аудиту на судостроительном комплексе "Звезда".
    Проанализируйте следующие данные о производстве (в формате JSON), включая финансовые показатели:
    ${dataJSON}

    Входящие данные содержат:
    - productionData: массив записей (выработка, брак, простой)
    - financialSummary: рассчитанные потери в рублях на основе стоимости брака и минуты простоя.

    Задачи:
    1. **Производственный анализ**: Выявите тренды производительности и смены с худшими показателями.
    2. **Финансовая аналитика**: 
       - Подробно опишите финансовые потери предприятия.
       - Укажите, что наносит больший ущерб: брак или простои.
       - Приведите итоговую сумму потерь за выбранный период.
    3. **График потерь**: Опишите словами, как выглядит график "Потери предприятия" (распределение потерь по дням или категориям), чтобы читатель мог это представить.
    4. **Рекомендации**: Дайте 3 рекомендации по снижению издержек.

    Ответ дайте в формате Markdown, используйте заголовки (##) и жирный шрифт для сумм (в рублях). Будьте профессиональным и кратким.
  `;

  try {
    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash',
      contents: prompt,
    });
    return response.text || "Не удалось получить ответ от модели.";
  } catch (error) {
    console.error("Gemini API Error:", error);
    return "Произошла ошибка при анализе данных. Проверьте консоль.";
  }
};